#include "libft.h"

int		ft_memcmp(const void *s1, const void *s2, size_t n)
{
	int		i;

	i = -1;
	while (++i < (int)n)
		if (((unsigned char *)s1)[i] != ((unsigned char *)s2)[i])
			return ((int)(((unsigned char *)s1)[i] - ((unsigned char *)s2)[i]));
	return (0);
}
